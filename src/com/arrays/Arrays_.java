package com.arrays;

public class Arrays_ {
	public static void main(String[] args) {
		boolean a[]=new boolean[2];
		a[0]=true;
		a[1]=false;
		
		for(boolean s:a)
			System.out.println(s);
		System.out.println("===*===");
		
	for(int i=0;i<a.length;i++) {
		System.out.println(a[i]);
	}
		
		
	}

}
