package com.collection.list;



public class CompareDemo  {
	
	int id;
	int marks;
	String name;
	
	
	
	public CompareDemo(int id, int marks, String name) {
		super();
		this.id = id;
		this.marks = marks;
		this.name = name;
	}
	



	@Override
	public String toString() {
		return "CompareDemo [id=" + id + ", marks=" + marks + ", name=" + name + "]";
	}

}


